package a_basic_variable;

public class B_variable {

	public static void main(String[] args) {

		int num; // 변수 선언
		int num2 = 20, num3 = 30; // 변수 선언과 초기화 동시에
		
		// System.out.println(num); 변수 num의 내용이 없어 오류
		
		num = 10; // 변수 초기화: 공간 num에 10을 넣어줌
		System.out.println(num);
		System.out.println(num2);
		System.out.println(num3);
		
	}

}
