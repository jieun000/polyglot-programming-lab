package a_basic_variable;

public class C_dataType {

	public static void main(String[] args) {

		int a = 10; // 정수
		double b = 3.14; // 실수
		char c = 'C'; // 문자
		String d = "hello"; // 문자열
		boolean e = true; // true, false
		
		System.out.print(a + " ");
		System.out.print(b + " ");
		System.out.print(c + " ");
		System.out.print(d + " ");
		System.out.println(e + " ");

		System.out.printf("정수: %d\n", a);
		System.out.printf("실수: %f\n", b);
		System.out.printf("문자: %c\n", c);
		System.out.printf("문자열: %s\n", d);
		System.out.printf("불린: %b\n", e);
		
	}

}
